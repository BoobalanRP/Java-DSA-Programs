package day23_Inhertence;

import java.util.Scanner;

public class CubeAndSquareGivenNumber {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		System.out.println("Enter an integer number: Square of " + a + " is: " + (a * a));
		System.out.println("Enter an integer number: Cube of " + b + " is: " + (b * b * b));

	}

}
